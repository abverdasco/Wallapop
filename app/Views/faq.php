    <h3 style="text-align: left; margin-left: 20%;" class="color-secun"><b>¿Es Wallapopper seguro?</b></h3><br>
    <p style="text-align: left; margin-left: 20%; margin-right: 20%;">
        Para Wallapopper la seguridad es una prioridad. Trabajamos día a día para que Wallapop sea una plataforma segura y seguimos buscando las mejores herramientas de seguridad para nuestros usuarios. <br><br>

        Wallapopper ofrece un inicio de sesión seguro mediante la verificación en 2 pasos. <br><br>

