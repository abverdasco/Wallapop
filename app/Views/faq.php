    <h3 style="text-align: left; margin-left: 20%;" class="color-secun"><b>Cómo funciona Wallapopper Envíos</b></h3><br>
    <p style="text-align: left; margin-left: 20%; margin-right: 20%;">
        El servicio de Wallapopper Envíos permite realizar envíos a corta y larga distancia de los productos publicados en Wallapop de forma segura para ambos usuarios. El servicio puede usarse tanto en la app como en la web de Wallapopper. <br><br>

        Los envíos se realizan con Correos, Seur, GLS o  Method (voluminosos), dependiendo del tipo de envío. <br><br>

        Ambos usuarios podrán seguir el curso del envío en la aplicación o en la web. También es posible consultar el estado del paquete desde el localizador de envíos de la web de Correos, en la web de Seur, en la web de GLS y en la web de Method introduciendo el código de seguimiento.
